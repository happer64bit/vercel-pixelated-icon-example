document.addEventListener("DOMContentLoaded", () => {
    const canvas = document.getElementById("mycanvas");
    const ctx = canvas.getContext("2d");

    const rectWidth = 10; // size of each "pixel"
    const rectHeight = 10;
    const width = data[0].length * rectWidth;
    const height = data.length * rectHeight;

    canvas.width = width;
    canvas.height = height;

    // Collect all black cell positions
    const blackCells = [];
    data.forEach((row, rowIndex) => {
        row.forEach((cell, cellIndex) => {
            if (cell === 1) {
                blackCells.push({ x: cellIndex * rectWidth, y: rowIndex * rectHeight });
            }
        });
    });

    // Draw the initial icon based on data array
    function drawIcon() {
        data.forEach((row, rowIndex) => {
            row.forEach((cell, cellIndex) => {
                if (cell === 1) {
                    ctx.fillStyle = "white";
                    ctx.beginPath();
                    ctx.arc(
                        cellIndex * rectWidth + rectWidth / 2,
                        rowIndex * rectHeight + rectHeight / 2,
                        rectWidth / 2.5,
                        0,
                        Math.PI * 2
                    );
                    ctx.fill();
                }
            });
        });
    }

    drawIcon(); // Draw the icon initially

    let hoverInterval;

    // Function to generate random gray spots on black cells only
    function drawRandomSpots() {
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        drawIcon();

        // Select 20 random black cell positions for gray spots
        const randomCells = [];
        while (randomCells.length < 50 && randomCells.length < blackCells.length) {
            const randomIndex = Math.floor(Math.random() * blackCells.length);
            const randomCell = blackCells[randomIndex];
            if (!randomCells.includes(randomCell)) {
                randomCells.push(randomCell);
            }
        }

        // Draw gray spots
        randomCells.forEach(cell => {
            ctx.fillStyle = "rgba(000, 000, 000, 0.6)"; // semi-transparent gray
            ctx.beginPath();
            ctx.arc(cell.x + rectWidth / 2, cell.y + rectHeight / 2, rectWidth / 2.5, 0, Math.PI * 2);
            ctx.fill();
        });
    }

    // Set up hover event listeners
    canvas.addEventListener("mouseover", () => {
        hoverInterval = setInterval(drawRandomSpots, 200);
    });

    canvas.addEventListener("mouseout", () => {
        clearInterval(hoverInterval);
        ctx.clearRect(0, 0, canvas.width, canvas.height);
        drawIcon(); 
    });
});
